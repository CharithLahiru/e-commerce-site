// Format price in Sri Lankan Rupees
function formatPrice(amount) {
  return 'Rs. ' + amount.toLocaleString('en-LK');
}

function getStars(rating) {
  const full = Math.floor(rating);
  const half = rating % 1 >= 0.5 ? '½' : '';
  return '★'.repeat(full) + half;
}
